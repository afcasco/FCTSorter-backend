package dev.afcasco.fctsorterbackend.entity;

import io.swagger.v3.oas.annotations.media.Schema;
import jakarta.persistence.*;
import jakarta.validation.constraints.NotBlank;
import lombok.*;
import org.hibernate.validator.constraints.Range;

@Entity
@AllArgsConstructor
@NoArgsConstructor
@Getter
@Setter
@ToString
public class Company {

    // TODO: 25/6/23 change zip code type from String to int or short 
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Schema(name = "Company ID",description = "Autogenerated value", example = "1")
    @Range(min=0)
    private Long id;
    @Schema(name = "CIF",description = "Company identification document", example = "B5555555")
    @NotBlank
    private String cif;
    @Schema(name = "Name",description = "Company name",example = "Compu-Global-Hyper-Mega-Net")
    @NotBlank
    private String name;
    @Schema(name = "Address",description = "Includes street, number and/or building name...", example = "742 Evergreen Terrace")
    @NotBlank
    private String address;
    @Schema(name = "City", description = "City name", example = "Springfield")
    @NotBlank
    private String city;
    @Schema(name = "Zip Code",description = "Zip code number",example = "80085")
    @NotBlank
    private String zipCode;
    @Schema(name = "Phone number", description = "Mobile/office telephone number", example = "555-6528")
    @NotBlank
    private String phone;
    @Schema(name = "Verified status", description = "Current registered status", example = "ACTIVE")
    @Enumerated(EnumType.STRING)
    private Status status;


}
